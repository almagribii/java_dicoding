package com.dicoding.operator;

public class OperasiAritmatika {
    public static void main(String[] args) {
        System.out.println("Operas Penjumalahan");
        int hasilPenjumlahan = 5 + 1;
        System.out.println("Hasil 5 + 1 = " + hasilPenjumlahan);
        System.out.println();

        System.out.println("Operasi Pengurangan");
        int hasilPengurangan = 10 - 8;
        System.out.println("Hasil 10 - 8 = " + hasilPengurangan);
        System.out.println();

        System.out.println("Operasi Pengalian");
        int hasilPengalian = 10 * 20;
        System.out.println("Hasil 10 * 20 = " + hasilPengalian);
        System.out.println();

        System.out.println("Operasi Pembagian");
        int hasilPembagian = 20 / 10;
        System.out.println("Hasil 20 / 10 = " + hasilPembagian);
        System.out.println();

        System.out.println("Operasi Habis Bagi");
        int habisBagi = 8 % 2;
        System.out.println("Hasil 8 % 2 = " + habisBagi);
        int hasilSisaLain = 9 % 2;
        System.out.println("Hasil 9 % 2 = " + hasilSisaLain);
        System.out.println();
    }
}
